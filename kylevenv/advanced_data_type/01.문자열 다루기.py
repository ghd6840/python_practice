# Replace
a = '요즘 날씨가 정말 춥다'.replace("춥다","덥다")
print(a)

# Find
b = 'Hello world'.find('Hellow')
print(b)

# split
c = 'Tops 110 XXL 50000'.split()
print(c)

d = 'Tops:110:XXL:50000'.split(':')
print(d)

# strip
e = '      Remove left space'.lstrip()
print(e)

f = 'Remove right space        '.rstrip()
print(f)

g = '        Remove space      '.strip()
print(g)