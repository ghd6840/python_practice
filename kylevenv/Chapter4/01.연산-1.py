# 산술연산
# + , - , * , / , //(몫) , % (나머지) , **(제곱)

# 숫자연산
x = 5
y = 2

print(x + y)
print(x - y)
print(x * y)
print(x / y)
print(x // y)
print(x % y)
print(x ** y)

